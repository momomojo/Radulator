#!/usr/bin/env node
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";
import { loadCalculatorInventory, resolveEdition } from "./resolve-edition.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const generatedDir = path.join(root, "src", "generated");

const categoryOrder = [
  "Radiology",
  "Neuroradiology",
  "Trauma",
  "Cardiac Imaging",
  "Breast Imaging",
  "Women's Imaging",
  "Clinical Decision",
  "Hepatology/Liver",
  "Urology",
  "Interventional",
  "Nephrology",
  "Feedback",
];

function js(value) {
  return JSON.stringify(value, null, 2);
}

function generateRegistry(edition, inventory) {
  let selected;
  if (edition.calculators === "all") {
    selected = inventory;
  } else {
    const byId = new Map(inventory.map((calc) => [calc.id, calc]));
    selected = edition.allowlist.map((id) => {
      const calc = byId.get(id);
      if (!calc) {
        throw new Error(`Institutional allowlist references unknown calculator id "${id}"`);
      }
      return calc;
    });
  }

  const imports = selected
    .map((calc) => {
      if (calc.exportName === "default") {
        const symbol = calc.file.replace(/\.jsx$/, "").replace(/\W/g, "_");
        return `import ${symbol} from "${calc.importPath}";`;
      }
      return `import { ${calc.exportName} } from "${calc.importPath}";`;
    })
    .join("\n");
  const symbols = selected
    .map((calc) =>
      calc.exportName === "default"
        ? calc.file.replace(/\.jsx$/, "").replace(/\W/g, "_")
        : calc.exportName,
    )
    .join(",\n  ");

  return `// Generated by scripts/generate-edition-artifacts.mjs.
// Do not edit by hand.

${imports ? `${imports}\n\n` : ""}export const calcDefs = [
  ${symbols}
].filter(Boolean).sort((a, b) => a.name.localeCompare(b.name));

export const categoryOrder = ${js(categoryOrder)};

export const categories = calcDefs.reduce((acc, calc) => {
  const category = calc.category || "Other";
  if (!acc[category]) acc[category] = [];
  acc[category].push(calc.id);
  return acc;
}, {});

export function getSortedCategories() {
  const allCategories = Object.keys(categories);
  const ordered = categoryOrder.filter((cat) => allCategories.includes(cat));
  const remaining = allCategories
    .filter((cat) => !categoryOrder.includes(cat))
    .sort();
  return [...ordered, ...remaining];
}

export function getCalculatorById(id) {
  return calcDefs.find((calc) => calc.id === id);
}

export function getCalculatorsByCategory(category) {
  const ids = categories[category] || [];
  return ids.map((id) => getCalculatorById(id)).filter(Boolean);
}

export const allTags = [
  ...new Set(calcDefs.flatMap((calc) => calc.tags || [])),
].sort();

export function getCalculatorsByTag(tag) {
  return calcDefs.filter((calc) => calc.tags?.includes(tag));
}

export default calcDefs;
`;
}

function generateEdition(edition) {
  const payload = {
    id: edition.id,
    title: edition.title,
    telemetryEnabled: Boolean(edition.telemetry?.ga4),
    feedbackEnabled: Boolean(edition.includeFeedback),
    externalLinks: edition.externalLinks,
    releaseControls: Boolean(edition.releaseControls),
    releaseControlFile: edition.releaseControlFile || "",
    releaseVersion: edition.releaseVersion || "",
    gitCommit: edition.gitCommit || "",
    calculatorAllowlist:
      edition.calculators === "all" ? "all" : [...edition.allowlist],
    calculatorAllowlistHash: edition.allowlistHash || "",
    validationManifestHash: edition.validationManifestHash || "",
    validationSchemaHash: edition.validationSchemaHash || "",
    persistCalculatorChoices: Boolean(edition.persistCalculatorChoices),
  };

  return `// Generated by scripts/generate-edition-artifacts.mjs.
// Do not edit by hand.

export const edition = Object.freeze(${js(payload)});

export default edition;
`;
}

function generateAnalytics(edition) {
  const target = edition.telemetry?.ga4
    ? "../lib/analytics.js"
    : "../lib/analytics-institutional.js";
  return `// Generated by scripts/generate-edition-artifacts.mjs.
// Do not edit by hand.

export {
  trackCalculatorSelected,
  trackCalculation,
  trackCSVDownload,
  trackOutboundLink,
  trackFeedbackSubmission,
  trackResultsCopied,
  trackResultViewed,
  trackOnboarding,
  trackSearch,
} from "${target}";
`;
}

export function generateEditionArtifacts({ env = process.env } = {}) {
  const edition = resolveEdition({ env, root });
  const inventory = loadCalculatorInventory(root);

  mkdirSync(generatedDir, { recursive: true });
  writeFileSync(
    path.join(generatedDir, "calculator-registry.generated.js"),
    generateRegistry(edition, inventory),
  );
  writeFileSync(
    path.join(generatedDir, "edition.generated.js"),
    generateEdition(edition),
  );
  writeFileSync(
    path.join(generatedDir, "analytics.generated.js"),
    generateAnalytics(edition),
  );

  const count = edition.calculators === "all" ? inventory.length : edition.allowlist.length;
  console.log(`Generated ${edition.id} edition artifacts (${count} calculator${count === 1 ? "" : "s"})`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generateEditionArtifacts();
}
